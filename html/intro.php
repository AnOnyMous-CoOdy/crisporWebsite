<div style="font-size:small;font-style:italics">
    <div class="title" style="display:inline;font-size:large;font-style: normal;">CRISPOR</div> 
    is a program that helps design and evaluate target sites for use with the CRISPR system.
    <br>
    It uses the BWA algorithm to identify the target sequences for use in CRISPR mediated genome editing.
    <br>
    It searches for off-target sites, shows them in a table and annotates them with flanking genes.
    <br>
